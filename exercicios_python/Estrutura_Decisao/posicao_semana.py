
entrada = int(input('Insira um número que corresponda posição na semana ou seja de 1 à 7: '))
if entrada == 1:
    print('Domingo')
elif entrada == 2:
    print('Segunda')
elif entrada == 3:
    print('Terça')
elif entrada == 4:
    print('Quarta')
elif entrada == 5:
    print('Quinta')
elif entrada == 6:
    print('Sexta')
elif entrada == 7:
    print('Sábado')
else:
    print('Entrada Inválida')

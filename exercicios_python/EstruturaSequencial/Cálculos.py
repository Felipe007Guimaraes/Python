primeiro = int(input('Informe um número inteiro: '))
segundo = int(input('Informe outro número inteiro: '))
terceiro = float(input('Agora insira um número real: '))
produto = (2*primeiro)*(segundo/2)
print(f'O produto do dobro do primeiro com a metade do segundo é {produto:.2f}')
soma = (3*primeiro)+(terceiro**3)
print(f'A soma do triplo do primeiro com o terceiro elevado ao cubo é {soma:.2f}')
cubo = terceiro**3
print(f'O terceiro elevado ao cubo é {cubo}')